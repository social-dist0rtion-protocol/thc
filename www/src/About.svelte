<h3>Why should I register an ENS name?</h3>
<p>
  Addresses are cool but they are very difficult to pronounce and remember.
  Worry not, if you register an ENS name <strong>on Görli</strong> you can
  customize your name and avatar in the leadeboard! How cool is that!
  <small>
    (The real reason to do it it's because we spent quite some time to integrate
    ENS in this app so please show some appreciation to our overly stressed devs
    and create an ENS entry and add a avatar image as well but please don't use
    the <code>ipfs://</code> schema or fancy NFT profile pics it would be cool ofc
    we are all here for decentralization but we didn't have enough time to integrate
    this PRs WELCOME!)
  </small>
</p>

<h3>How do I register my ENS name?</h3>
<p>
  Export your mnemonic to another wallet, go to the ENS domain and register your
  name in the <strong>Görli network</strong>. You can also add an avatar (we
  only support `http` and `https` schemes).
</p>

